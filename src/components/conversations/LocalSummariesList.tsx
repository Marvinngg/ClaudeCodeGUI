'use client';

import { useState, useEffect } from 'react';
import { useRouter } from 'next/navigation';
import { HugeiconsIcon } from '@hugeicons/react';
import {
  File01Icon,
  Delete02Icon,
  Loading02Icon,
  Calendar03Icon,
  ArrowRight01Icon,
  Download01Icon,
  CloudUploadIcon,
} from '@hugeicons/core-free-icons';
import { Button } from '@/components/ui/button';
import { Badge } from '@/components/ui/badge';
import { Card, CardContent, CardDescription, CardHeader, CardTitle } from '@/components/ui/card';
import { useLanguage } from '@/contexts/LanguageContext';

interface Conversation {
  id: string;
  session_id: string;
  name: string;
  description: string;
  content: string;
  tags: string[];
  raw_messages?: string; // JSON string of original messages
  source?: 'local' | 'hub'; // 'local' for locally created, 'hub' for installed from Hub
  created_at: string;
  updated_at: string;
}

export function LocalSummariesList() {
  const [conversations, setConversations] = useState<Conversation[]>([]);
  const [loading, setLoading] = useState(true);
  const [selectedId, setSelectedId] = useState<string | null>(null);
  const [selectedConversation, setSelectedConversation] = useState<Conversation | null>(null);
  const [filterSource, setFilterSource] = useState<'all' | 'local' | 'hub'>('all');
  const router = useRouter();
  const { t } = useLanguage();

  useEffect(() => {
    loadConversations();
  }, []);

  const loadConversations = async () => {
    setLoading(true);
    try {
      const res = await fetch('/api/conversations');
      if (!res.ok) throw new Error('Failed to load conversations');
      const data = await res.json();
      setConversations(data.conversations || []);
    } catch (error) {
      console.error('[LocalSummariesList] Load error:', error);
    } finally {
      setLoading(false);
    }
  };

  const handleSelectConversation = async (id: string) => {
    setSelectedId(id);
    try {
      const res = await fetch(`/api/conversations/${id}`);
      if (!res.ok) throw new Error('Failed to load conversation');
      const data = await res.json();
      setSelectedConversation(data.conversation);
    } catch (error) {
      console.error('[LocalSummariesList] Select error:', error);
      alert(`加载失败：${error instanceof Error ? error.message : 'Unknown error'}`);
    }
  };

  const handleDelete = async (id: string, e?: React.MouseEvent) => {
    e?.stopPropagation();
    if (!confirm('确定要删除这个总结吗？')) return;

    try {
      const res = await fetch(`/api/conversations/${id}`, {
        method: 'DELETE',
      });
      if (!res.ok) throw new Error('Failed to delete conversation');

      // 刷新列表
      await loadConversations();

      // 如果删除的是当前选中的，清空详情
      if (selectedId === id) {
        setSelectedId(null);
        setSelectedConversation(null);
      }
    } catch (error) {
      console.error('[LocalSummariesList] Delete error:', error);
      alert(`删除失败：${error instanceof Error ? error.message : 'Unknown error'}`);
    }
  };

  const handleOpenChat = (sessionId: string, e?: React.MouseEvent) => {
    e?.stopPropagation();
    router.push(`/chat/${sessionId}`);
  };

  const handleDownload = async (conversation: Conversation, e?: React.MouseEvent) => {
    e?.stopPropagation();

    try {
      // 生成 Markdown 内容
      const markdown = `# ${conversation.name}

> ${conversation.description}

${conversation.tags.length > 0 ? `**Tags:** ${conversation.tags.join(', ')}\n\n` : ''}
**Created:** ${formatDate(conversation.created_at)}
**Updated:** ${formatDate(conversation.updated_at)}
**Session ID:** ${conversation.session_id}

---

${conversation.content}

---

_Generated by CodePilot_
`;

      // 创建下载链接
      const blob = new Blob([markdown], { type: 'text/markdown;charset=utf-8' });
      const url = URL.createObjectURL(blob);
      const link = document.createElement('a');
      link.href = url;
      link.download = `${conversation.name.replace(/[^a-zA-Z0-9\u4e00-\u9fa5]/g, '_')}.md`;
      document.body.appendChild(link);
      link.click();
      document.body.removeChild(link);
      URL.revokeObjectURL(url);
    } catch (error) {
      console.error('[LocalSummariesList] Download error:', error);
      alert(`下载失败：${error instanceof Error ? error.message : 'Unknown error'}`);
    }
  };

  const handlePublishToHub = async (conversation: Conversation, e?: React.MouseEvent) => {
    e?.stopPropagation();

    // 确认发布
    if (!confirm(`确定要将"${conversation.name}"发布到 Hub 吗？\n\n发布后其他人可以在 Hub 中浏览和下载这个总结。`)) {
      return;
    }

    try {
      // 发布到 Hub（包含原始对话）
      const res = await fetch('/api/hub/conversations', {
        method: 'POST',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify({
          title: conversation.name,
          content: conversation.content,
          description: conversation.description,
          tags: conversation.tags.join(','),
          raw_messages: conversation.raw_messages || null, // ✅ 包含原始对话
        }),
      });

      if (!res.ok) {
        const data = await res.json();
        throw new Error(data.error || 'Failed to publish to Hub');
      }

      alert('发布成功！\n\n已上传到 Hub，其他人现在可以在 Hub 中查看和下载。');
    } catch (error) {
      console.error('[LocalSummariesList] Publish error:', error);

      // 友好的错误提示
      const errorMsg = error instanceof Error ? error.message : 'Unknown error';
      if (errorMsg.includes('Hub URL not configured')) {
        alert('发布失败：未配置 Hub URL\n\n请前往 Settings → Hub Settings 配置 Hub 服务器地址。');
      } else if (errorMsg.includes('User Identifier')) {
        alert('发布失败：未配置用户标识\n\n请前往 Settings → Hub Settings 设置 User Identifier。');
      } else {
        alert(`发布失败：${errorMsg}`);
      }
    }
  };

  const formatDate = (dateStr: string) => {
    const date = new Date(dateStr);
    return date.toLocaleString('zh-CN', {
      year: 'numeric',
      month: '2-digit',
      day: '2-digit',
      hour: '2-digit',
      minute: '2-digit',
    });
  };

  if (loading) {
    return (
      <div className="flex items-center justify-center h-64">
        <HugeiconsIcon icon={Loading02Icon} className="h-8 w-8 animate-spin text-muted-foreground" />
      </div>
    );
  }

  // ✅ 过滤对话
  const filteredConversations = conversations.filter((conv) => {
    if (filterSource === 'all') return true;
    return conv.source === filterSource;
  });

  return (
    <div className="flex h-full">
      {/* 左侧：列表 */}
      <div className="w-1/3 border-r border-border overflow-y-auto">
        <div className="p-4 border-b border-border">
          <h2 className="text-lg font-semibold">{t('Conversation Summaries')}</h2>
          <p className="text-sm text-muted-foreground">{t('total conversations').replace('{{count}}', String(filteredConversations.length))}</p>

          {/* ✅ 分类过滤器 */}
          <div className="flex gap-1 mt-2">
            <Button
              variant={filterSource === 'all' ? 'default' : 'ghost'}
              size="sm"
              onClick={() => setFilterSource('all')}
              className="text-xs h-7"
            >
              {t('All')} ({conversations.length})
            </Button>
            <Button
              variant={filterSource === 'local' ? 'default' : 'ghost'}
              size="sm"
              onClick={() => setFilterSource('local')}
              className="text-xs h-7"
            >
              {t('Local')} ({conversations.filter(c => c.source === 'local').length})
            </Button>
            <Button
              variant={filterSource === 'hub' ? 'default' : 'ghost'}
              size="sm"
              onClick={() => setFilterSource('hub')}
              className="text-xs h-7"
            >
              Hub ({conversations.filter(c => c.source === 'hub').length})
            </Button>
          </div>
        </div>

        <div className="divide-y divide-border">
          {filteredConversations.length === 0 ? (
            <div className="p-8 text-center text-muted-foreground">
              <HugeiconsIcon icon={File01Icon} className="h-12 w-12 mx-auto mb-2 opacity-50" />
              <p>{t('No conversation summaries')}</p>
              <p className="text-xs mt-1">{t('Click "Summarize Conversation" in chat to create')}</p>
            </div>
          ) : (
            filteredConversations.map((conv) => (
              <div
                key={conv.id}
                className={`p-4 cursor-pointer hover:bg-accent transition-colors group ${
                  selectedId === conv.id ? 'bg-accent' : ''
                }`}
                onClick={() => handleSelectConversation(conv.id)}
              >
                <div className="flex items-start justify-between gap-2">
                  <div className="flex-1 min-w-0">
                    <div className="flex items-center gap-2">
                      <h3 className="font-medium truncate">{conv.name}</h3>
                      {/* ✅ 只显示来源标识 */}
                      {conv.source === 'hub' && (
                        <Badge variant="outline" className="text-xs shrink-0">
                          {t('From Hub')}
                        </Badge>
                      )}
                    </div>
                    <p className="text-sm text-muted-foreground line-clamp-2 mt-1">
                      {conv.description}
                    </p>
                    <div className="flex items-center gap-2 mt-2">
                      <HugeiconsIcon icon={Calendar03Icon} className="h-3 w-3 text-muted-foreground" />
                      <span className="text-xs text-muted-foreground">
                        {formatDate(conv.updated_at)}
                      </span>
                    </div>
                  </div>
                  <div className="flex items-center gap-1 opacity-0 group-hover:opacity-100 transition-opacity">
                    <Button
                      variant="ghost"
                      size="icon"
                      className="h-8 w-8"
                      onClick={(e) => handleDownload(conv, e)}
                      title={t('Download Markdown')}
                    >
                      <HugeiconsIcon icon={Download01Icon} className="h-4 w-4" />
                    </Button>
                    <Button
                      variant="ghost"
                      size="icon"
                      className="h-8 w-8"
                      onClick={(e) => handlePublishToHub(conv, e)}
                      title={t('Upload to Hub')}
                    >
                      <HugeiconsIcon icon={CloudUploadIcon} className="h-4 w-4" />
                    </Button>
                    <Button
                      variant="ghost"
                      size="icon"
                      className="h-8 w-8"
                      onClick={(e) => handleOpenChat(conv.session_id, e)}
                      title={t('Open Chat')}
                    >
                      <HugeiconsIcon icon={ArrowRight01Icon} className="h-4 w-4" />
                    </Button>
                    <Button
                      variant="ghost"
                      size="icon"
                      className="h-8 w-8 text-destructive"
                      onClick={(e) => handleDelete(conv.id, e)}
                      title={t('Delete')}
                    >
                      <HugeiconsIcon icon={Delete02Icon} className="h-4 w-4" />
                    </Button>
                  </div>
                </div>

                {conv.tags.length > 0 && (
                  <div className="flex flex-wrap gap-1 mt-2">
                    {conv.tags.slice(0, 3).map((tag) => (
                      <Badge key={tag} variant="secondary" className="text-xs">
                        {tag}
                      </Badge>
                    ))}
                    {conv.tags.length > 3 && (
                      <Badge variant="secondary" className="text-xs">
                        +{conv.tags.length - 3}
                      </Badge>
                    )}
                  </div>
                )}
              </div>
            ))
          )}
        </div>
      </div>

      {/* 右侧：详情 */}
      <div className="flex-1 overflow-y-auto">
        {selectedConversation ? (
          <div className="p-6">
            <Card>
              <CardHeader>
                <div className="flex items-start justify-between">
                  <div className="flex-1">
                    <div className="flex items-center gap-2">
                      <CardTitle>{selectedConversation.name}</CardTitle>
                      {/* ✅ 只显示来源标识 */}
                      {selectedConversation.source === 'hub' && (
                        <Badge variant="outline">
                          {t('From Hub')}
                        </Badge>
                      )}
                    </div>
                    <CardDescription className="mt-2">
                      {selectedConversation.description}
                    </CardDescription>
                  </div>
                  <div className="flex items-center gap-1">
                    <Button
                      variant="ghost"
                      size="sm"
                      onClick={() => handleDownload(selectedConversation)}
                      title={t('Download Markdown')}
                    >
                      <HugeiconsIcon icon={Download01Icon} className="h-4 w-4" />
                    </Button>
                    <Button
                      variant="ghost"
                      size="sm"
                      onClick={() => handlePublishToHub(selectedConversation)}
                      title={t('Upload to Hub')}
                    >
                      <HugeiconsIcon icon={CloudUploadIcon} className="h-4 w-4" />
                    </Button>
                    <Button
                      variant="ghost"
                      size="sm"
                      onClick={() => handleOpenChat(selectedConversation.session_id)}
                      title={t('Open Chat')}
                    >
                      <HugeiconsIcon icon={ArrowRight01Icon} className="h-4 w-4 mr-1" />
                      {t('Open Chat')}
                    </Button>
                    <Button
                      variant="ghost"
                      size="sm"
                      onClick={() => handleDelete(selectedConversation.id)}
                    >
                      <HugeiconsIcon icon={Delete02Icon} className="h-4 w-4" />
                    </Button>
                  </div>
                </div>

                {selectedConversation.tags.length > 0 && (
                  <div className="flex flex-wrap gap-2 mt-4">
                    {selectedConversation.tags.map((tag) => (
                      <Badge key={tag} variant="outline">
                        {tag}
                      </Badge>
                    ))}
                  </div>
                )}

                <div className="flex items-center gap-4 mt-4 text-sm text-muted-foreground">
                  <div className="flex items-center gap-1">
                    <HugeiconsIcon icon={Calendar03Icon} className="h-4 w-4" />
                    <span>{t('Created')}：{formatDate(selectedConversation.created_at)}</span>
                  </div>
                  {selectedConversation.updated_at !== selectedConversation.created_at && (
                    <div className="flex items-center gap-1">
                      <HugeiconsIcon icon={Calendar03Icon} className="h-4 w-4" />
                      <span>{t('Updated')}：{formatDate(selectedConversation.updated_at)}</span>
                    </div>
                  )}
                </div>
              </CardHeader>

              <CardContent>
                <div className="prose prose-sm max-w-none dark:prose-invert">
                  <div className="whitespace-pre-wrap">{selectedConversation.content}</div>
                </div>
              </CardContent>
            </Card>
          </div>
        ) : (
          <div className="flex items-center justify-center h-full text-muted-foreground">
            <div className="text-center">
              <HugeiconsIcon icon={File01Icon} className="h-16 w-16 mx-auto mb-4 opacity-30" />
              <p>{t('Select a conversation to view details')}</p>
            </div>
          </div>
        )}
      </div>
    </div>
  );
}
